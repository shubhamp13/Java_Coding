class Division{

	public static void main(String []args)
	{
		//Division Of Two Bytes Will Result Into Integer Only
		byte b1=10;
		byte b2=10;
		int b3=b1/b2;
		System.out.println(b3);

		//Division Of Two short Will Result Into Integer Only
		short s1=120;
		short s2=12;
		int s3=s1/s2;
		System.out.println(s3);

		//Division  Of Two Integer Will Result Only Into Integer
		int i1=110;
		int i2=11;
		int i3=i1/i2;
		System.out.println(i3);

		//Divison Of Two Long Will Result Into Long Only

		long l1=1000l;
		long l2=10l;
		long l3=l1/l2;
		System.out.println(l3);

		//Division Of Two Charcter Will Result into integer Only
		char c1='z';
		char c2='a';
		int c3=c1/c2;
		System.out.println(c3);

		 //Division Of Two Float will Result Only In Float
		float f1=3.1f;
		float f2=1.1f;
		float f3=f1/f2;
		System.out.println(f3);

		// Division Of Two Double will result only in Double

		double d1=10.2;
		double d2=1.12;
		double d3=d1/d2;
		System.out.println(d3);

		//Division Of Two Strings Will Be Result An Error Of Bad Operand Types Of Binary Operator "/"
		// String str1="Shubham";
		// String str2="Puri";
		// int str3=str1/str2;
		// System.out.println(str3);

	



	}
}