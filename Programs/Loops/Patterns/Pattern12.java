class Pattern12//(2,1)//(3,1)(3,2)//(4,1)(4,2)(4,3)()
{
	public static void main(String[] args) 
	{
		for(int i=0;i<6;i++)
		{
			for(int j=0;j<=i;j++)
			{
				
				if(i>1&&i!=5)
				{
					if(j==0||j==i)
					{
						System.out.print("*"+" ");
					}
					else
					{
						System.out.print(" "+" ");
					}
				}
				else
				{
					System.out.print("*"+" ");
				}
			 
			}
			System.out.println();
		}
	}
}