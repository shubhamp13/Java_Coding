class CharDataTypeCasting
{
	public static void main(String[] args) 
	{
		
		char c='A';
		int i=c;
		long l=c;
		float f=c;
		double d=c;
	 
		System.out.println(c);//A
	
		System.out.println(i);//65

		System.out.println(l);//65

		System.out.println(f);//65.0

		System.out.println(d);//65.0



		
	}
	
}