class Explicit{
	public static void main(String[] args) {
		byte b=10;
		short s=(short)b;
		int i=(int )b;
		long l=(long)b;
		float f=(float)b;
		char c=(char)b;
		double d=(double)b;
		System.out.println(b);
		System.out.println(s);
		System.out.println(i);
		System.out.println(l);
		System.out.println(f);
		System.out.println(c);
		System.out.println(d);



	}
}